            //String tabla = "marcaciones_dic_2019";
            System.out.println(tabla);

            marcacion = new Marcacion();

            marcacion.setTarjeta(tarjeta);
            marcacion.setAgente(agente);
            marcacion.setLegajo(tarjeta);
