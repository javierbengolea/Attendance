     //       System.out.println(temp);
            
      //      System.err.println(temp);
    //        marcacion.setFecha(temp);

            marcacion.setHora(hora);
            marcacion.setFechaProceso(new Fecha());
            marcacion.setReloj(reloj);
            marcacion.setCodigo(codigo);
            marcacion.setTipo(2);

