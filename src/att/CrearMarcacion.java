    public static String[] getNombres() {
        String[] nombres = {"Agente a", "Agente b"};

        try {

            nombres = new String[agentes.size()];

            //   System.out.println(agentes.keySet());
            for (int i = 0; i < nombres.length; i++) {

                nombres[i] = (String) agentes.get(claves[i]);
            }

        } catch (Exception e) {
            ArrayList<StackTraceElement> stea = new ArrayList<>();

            stea.addAll(Arrays.asList(e.getStackTrace()));

            System.out.println(stea);
        }
        return nombres;
    }

